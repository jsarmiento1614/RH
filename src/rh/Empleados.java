public class Empleados {
    private String nombre;
    private String Sueldo;

    public Empleados(String nombre, String Sueldo) {
        this.nombre = nombre;
        this.Sueldo = Sueldo;
    }

    public String getNombre() {
        return nombre;
    }

    public String getSueldo() {
        return Sueldo;
    }
    
    
    
    
}
